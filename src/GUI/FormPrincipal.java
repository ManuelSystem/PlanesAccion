    private void cargarPrivilegios(ArrayList<String> privilegios){
        
        String hola;
        hola=privilegios.get(0);
        
        String[] parts = hola.split("/");
        part3 = parts[2];

        for (String privilegio : privilegios) {
            
            switch (privilegio.split("/")[0]){
                
                case "iniP":
                    
                    this.tpnPrincipal.setEnabledAt(0, true);
                    break;
                  
                case "gesM":
                    
                    this.tpnPrincipal.setEnabledAt(1, true);
                    break;
                
                case "gesT":
                    this.tpnPrincipal.setEnabledAt(2, true);
                    break;
                    

            }
            
            switch (privilegio.split("/")[1]){
                
                //modulo gestion de matrices
                
                    
                case "RF02-VisuMat":
                    this.tpnGestionMatriz.setSelectedIndex(1);
                    this.tpnGestionMatriz.setEnabledAt(1, true);  
                    break;
                    
                case "RF01-NewMatr":
                    this.tpnGestionMatriz.setSelectedIndex(0);
                    this.tpnGestionMatriz.setEnabledAt(0, true);
                    this.tpnNuevaMatriz.setEnabledAt(0, true);
                    break;
                    
                case "RF03-Vlistad":
                    this.tpnVisualizarMatriz.setEnabledAt(0, true);
                    break;

                case "RF04-VerMatr":
                    this.btnAgregarValoracion.setEnabled(true);
                    this.tpnVisualizarMatriz.setEnabledAt(1, false);
                    break;
                    
                case "RF05-CrearMa":
                    this.tpnNuevaMatriz.setEnabledAt(0, true);
                    break;
                    
                case "RF06-AgAnali":
                    this.tpnNuevaMatriz.setEnabledAt(1, false);
                    break;
                    
                case "RF07-AggAcci":
                    this.tpnNuevaMatriz.setEnabledAt(2, false);
                    break;
                    
                case "RF08-AggTare":
                    this.tpnNuevaMatriz.setEnabledAt(3, false);
                    break;
                    
  
            }
               
        }
        
    }
    
