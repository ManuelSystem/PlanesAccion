    private void generarPDF(String idPlanAccion,String nom,String fechaInicio,String fechaFinal,String respon,String idCondi) throws FileNotFoundException, DocumentException{
        
        //Se crea el documento
        Document documento = new Document();
        
        //se crea fichero
        FileOutputStream ficheroPDF = new FileOutputStream(nom+".pdf");
        
        //se asocian
        PdfWriter.getInstance(documento, ficheroPDF);
        
        //se abre el documento
        documento.open();
        
        //foto
        try
        {
                Image foto = Image.getInstance("src/IMG/8.jpg");
                Image foto1 = Image.getInstance("src/IMG/2.png");
                foto.scaleToFit(150, 150);
                foto.setAlignment(Chunk.ALIGN_LEFT);
                foto1.scaleToFit(110, 110);
                foto1.setAbsolutePosition(480f, 700f);
                documento.add(foto);
                documento.add(foto1);
        }
        catch ( Exception e )
        {
                e.printStackTrace();
        }
        
        //parrafo
        Paragraph titulo = new Paragraph("\n Ingenieria de Sistemas \n\n",
                FontFactory.getFont("Arial",20,Font.BOLD,BaseColor.BLUE)
        );
        
        //titulo al documento
        documento.add(titulo);
        
        //adds
        documento.add(new Paragraph("Matriz con toda su información. \n\n"));
        documento.add(new Paragraph("Nombre: "+nom+"\n"));
        documento.add(new Paragraph("Fecha Inicio Planeada: "+fechaInicio+"                     "+"Fecha Final Planeada: "+fechaFinal+"\n"));
        documento.add(new Paragraph("Responsable de Seguimiento: "+respon+"\n\n"));
        
        //ahora la tabla
        PdfPTable tabla = new PdfPTable(5);
        tabla.addCell("ID");
        tabla.addCell("Nombre");
        tabla.addCell("Fecha Inicio");
        tabla.addCell("Fecha Final");
        tabla.addCell("Responsable Seguimiento");
        
        //datos
        tabla.addCell(idPlanAccion);
        tabla.addCell(nom);
        tabla.addCell(fechaInicio);
        tabla.addCell(fechaFinal);
        tabla.addCell(respon);
        
        //añadimos la tabla
        documento.add(tabla);
        
        documento.close();
        
        documento.isOpen();
        
    }
    
