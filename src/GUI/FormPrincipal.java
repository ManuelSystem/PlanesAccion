    //abrir para agregar valoracion
    private void agregarValoracion(){
        
        BorderLayout layaut = new BorderLayout();
        
        //Se crea un panel
        JPanel panel = new JPanel();
        panel.setLayout(new BoxLayout(panel,BoxLayout.Y_AXIS));
        
        //se crea un nuevo JTextArea
        JTextArea salida = new JTextArea(5,30);
        salida.setLineWrap(true);
        // se crea un nuevo scrollPane
        JScrollPane scrollPane = new JScrollPane(salida);
        
        //demas componentes
        JLabel label1 = new JLabel();
        label1.setText("Seleccione una evaluación:");
        
        JComboBox combo = new JComboBox();
        String uno = "Seleccione";
        String dos = "Aprobado";
        String tres = "Con Observaciones";
        String cuatro = "Rechazado";
        combo.addItem(uno);
        combo.addItem(dos);
        combo.addItem(tres);
        combo.addItem(cuatro);
        
        JLabel label2 = new JLabel();
        label2.setText("Digite sus observaciones:");
        
        //agregar al panel
        panel.add(label1);
        panel.add(combo);
        panel.add(new JLabel("               "));
        panel.add(label2);
        panel.add(salida);
        
        //Se muestra el texto en un JOptionPane
        JOptionPane.showMessageDialog(null, panel, "VALORACIÓN", JOptionPane.INFORMATION_MESSAGE);
        
        if(combo.getSelectedItem().equals("Seleccione") || salida.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Complete los campos");
            JOptionPane.showMessageDialog(null, panel, "VALORACIÓN", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
            String tipoValoracion = combo.getSelectedItem().toString();
            String observacion = salida.getText();
            
            llenarTablaValoracionPlan(tipoValoracion, observacion, idPlanAccion);
            JOptionPane.showMessageDialog(null, "Gracias por llenar los campos");
        
    }

